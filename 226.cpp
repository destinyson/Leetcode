/**************************************226 - 翻转二叉树*************************************

翻转一棵二叉树。

示例：

输入：

     4
   /   \
  2     7
 / \   / \
1   3 6   9
输出：

     4
   /   \
  7     2
 / \   / \
9   6 3   1
*****************************************************************************************************/

//递归翻转二叉树

class Solution {
public:
    TreeNode* invertTree(TreeNode* root) {
        if (!root)
            return NULL;
        else
        {
            root->left = invertTree(root->left);
            root->right = invertTree(root->right);
            swap(root->left, root->right);
            return root;
        }
    }
};
