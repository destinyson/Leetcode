/**************************************172 - 阶乘后的零*************************************

给定一个整数 n，返回 n! 结果尾数中零的数量。

示例 1:

输入: 3
输出: 0
解释: 3! = 6, 尾数中没有零。

示例 2:

输入: 5
输出: 1
解释: 5! = 120, 尾数中有 1 个零.

说明: 你算法的时间复杂度应为 O(log n) 。
*****************************************************************************************************/

//10=2*5，末尾0的个数取决于2与5的因子数，而显然2的因子数远远大于5的因子数

//因此只需要考察阶乘中有多少因子5

class Solution {
public:
    int trailingZeroes(int n) {
        int ans = 0;
        int flag = 5;
        while (n)
        {
            ans = ans + n / 5;
            n = n / 5;
        }
        return ans;
    }
};
