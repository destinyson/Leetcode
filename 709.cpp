/**************************************709 - 转换成小写字母*************************************

实现函数 ToLowerCase()，该函数接收一个字符串参数 str，并将该字符串中的大写字母转换成小写字母，之后返回新的字符串。



示例 1：

输入: "Hello"
输出: "hello"

示例 2：

输入: "here"
输出: "here"

示例 3：

输入: "LOVELY"
输出: "lovely"
*****************************************************************************************************/

//遍历字符串，大写字母转换成对应小写字母即可

class Solution {
public:
    string toLowerCase(string str) {
        int len = str.length();
        for (int i = 0; i < len; i++)
        {
            if (str[i] >= 'A' && str[i] <= 'Z')
                str[i] = str[i] + 32;
        }
        return str;
    }
};
