/**************************************1299 - 将每个元素替换为右侧最大元素*************************************

给你一个数组 arr ，请你将每个元素用它右边最大的元素替换，如果是最后一个元素，用 -1 替换。

完成所有替换操作后，请你返回这个数组。


示例：

输入：arr = [17,18,5,4,6,1]
输出：[18,6,6,6,1,-1]


提示：

1 <= arr.length <= 10^4
1 <= arr[i] <= 10^5
*****************************************************************************************************/

//从后向前遍历数组，比较赋值

class Solution {
public:
    vector<int> replaceElements(vector<int>& arr) {
        int num = arr.size();
        vector<int> ans(num);
        int max = -1;
        ans[num - 1] = -1;
        for (int i = num - 2; i >= 0; i--)
        {
            max = max > arr[i + 1] ? max : arr[i + 1];
            ans[i] = max;
        }
        return ans;
    }
};
